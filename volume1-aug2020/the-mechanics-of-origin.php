                    	<p>In their piece “Nativity,” poet Xandria Phillips writes: “I haven’t yet begun to unweld the framework, invent new trauma, whip the stitch arching each bosom...where have I gone, and who have I built to take my place?...I am every man I manufactured my distance from” (2019). Phillips concentrates suffering into a complex body that, in actively developing, defiles the shape of its victim. Its contours materialize quietly under scrutiny, sculpted by what others find repulsive about human nature. Thus, the body finally reflects what anonymously gnaws at a community’s collective conscience. Phillip’s lines represent the most obvious similarity between Beloved and the Scarlet Letter: the growth and ubiquity of trauma. Beloved bloats as the flesh between Sethe’s “forefinger and thumb” fades (242). The scarlet letter appears in “exaggerated and gigantic proportions,” obfuscating Hester and emaciating Dimmesdale’s frame (97). In the Scarlet Letter, Puritan children’s taunts imply they assign an anthropomorphized A, the “scarlet little figure” Pearl (flitting about without a bosom to anchor herself), to their community. Hester’s letter’s “sympathetic throbs” detect an evil so pervasive it infiltrates even the saintliest of men (80). Indeed, Pearl’s “one baby-voice” serves a “multitude of imaginary personages, old and young”, a harmonious utterance of shame (87). Beloved’s footprints are so ”familiar” and universal that “should that a child” or “an adult place their feet in them, they will fit” (275). Ultimately, only the united prayers and song of the community can expel a parasitic Beloved from 124; the child is not Sethe’s demon alone (261). However, remarkably, Hawthorne and Morrison both use nature and color in motion, much as Phillips envisions trauma “welding,” “inventing,” and “whipping,” to express why this pained body continues to grow. In Beloved and the Scarlet Letter the progeny of sin1 interact with and harness an aspect of nature to force others to confront their traumas.</p>
<p>
Beloved and Pearl gather plants and strike others with nature to alert them to their past traumas. Morrison specifically associates collecting plants in a basket-like repository with birth and reconciliation with the past. Three-week-old Denver “lays in a bushel basket” beside Baby Suggs, and Sweet Home women “hang babies in” baskets while they toil (138, 160). In a stream of consciousness, Beloved speaks of a woman (i.e. Sethe), who “fills the basket” with “leaves”, memories of her children who left her, that she collects from the “open grass”, or her expansive mindscape (210). In Beloved’s fantasy where she and Sethe are “joined” and “smiling”, there is “there is no round basket;” Beloved wants to “help” her with the basket, or alleviate her mother’s anguish, but she laments that “the clouds are in the way” (210, 213). As Beloved unhealthily consumes Sethe’s life, she physically resembles a bulging basket of burden, likened to yet another plant: “a winning watermelon” (250). However, Sethe fails to notice Beloved’s “basket-fat stomach” collecting plants, so Beloved becomes increasingly abhorrent. She tries to signal to Sethe that obsessively harping on her past is ruinous, breaking “window panes” and “plates” in an ineffable rage (242). Likewise, in the Scarlet Letter, Chillingsworth tells Dimmesdale that “black”, ugly weeds spring “from a buried heart” (119). These secret-harborers are birthed anew (or, at least, cannot rest), as their hearts extend once more into the living world. Pearl gathers and uproots “the ugliest weeds” for pleasure (87). She eliminates these people’s unresolved past trauma by forcefully yanking their secrets from their chests. Additionally, Pearl and Beloved harness nature to shock others into constantly recalling their traumas. Pearl collects “the prickly burrs from the burdock” growing “beside the tombstone” and places them “along the lines of the scarlet letter” on Hester’s “maternal bosom” (122). She also destructively flings them at Dimmesdale, who shrinks away “with nervous dread” (122). Pearl ardently prevents her mother from forgetting about her mark of shame, especially as the burrs “tenaciously [adhere]” to clothing, so Hester “does not pluck them off” (122). Pearl even gleefully attacks Dimmesdale, who is publicly immune to this shame, with the same “instrument” she uses to terrorize Hester. Similarly, when Sethe enters a dream-like state to feel “Baby Sugg’s fingers molding her nape,” Beloved employs the wind to strangle her (86). Beloved refuses to let Sethe “lay all of the mess down”, and she literally impresses upon Sethe she cannot neglect her past (86). After Sethe regains her breath, Beloved strokes the splotches forming on her neck and “gathers color” there “darker than [her] throat” (97). Like Pearl, Beloved even leaves a temporary mark on Sethe’s body to dissuade her from slipping into a state of blissful ignorance.</p>
<p>
Pearl and Beloved use properties of water to make others face their traumas. Watery blue characterizes retribution that hounds Sethe and Dimmesdale from their past. For sustenance at the bottom (of what I am choosing to interpret as) the creek, Beloved “sucks sweet rocks'' and consumes “sea-colored” bread; she cannot “make sweat or morning water” (210). When Sethe first glimpses Beloved, she rushes to the back of 124, and the blue “water she voided was endless” (51). Beloved gives Sethe an uncomfortable excess of what she was deprived of. Immediately, Sethe is harkened back to her trauma; she had not had an “emergency this unmanageable” since she was “a little girl” (51). Sethe also connects the water to “flooding the boat when Denver was born” (51). Similarly, Chillingsworth’s eyes glimmer “blue and ominous” with vengeance (caused by the birth of Pearl) that plagues Dimmesdale (118). Their blueness is particularly salient when he “searches” Dimmesdale's room, “turning over many precious materials” to find a scarlet letter (118). Moreover, the undulation of water (controlled by Pearl and Beloved) initiates forms of “self” flagellation. When Dimmesdale tortures himself with nightly vigils, he is haunted by the “ghost of a mother—thinnest fantasy of a mother” (132). Water affords Pearl control over spirits; she once “beckons a phantom forth” by playing with her image in a pool (160). Her intellect is guided by the “tide of life”, just as Dimmesdale’s ghosts arrive and “flow heavily upward” (87, 132). Attuned to phantomly waves, Pearl sends Dimmesdale the image of his ashamed mother to persecute him for abandoning Hester, a single mother. In Beloved, Paul D likens Beloved to a violent current when they have sex, pushing him under waves of “personal shame and repulsion,” and “every time she” comes renders him “beached and gobbling air” (264). However, afterwards, he admits he had been “escorted to some ocean-deep place he once belonged” for self-reflection and examination of his past trauma (264).</p>
<p>
At last, a drained Sethe retires to Baby Suggs’ room with no figure from her past really left to cling to. In the Scarlet Letter, loneliness is “the destiny that had drawn an inviolable circle round about” Pearl and Hester (85). But, “around every circle another can be drawn; that there is no end in nature, but every end is a beginning; that there is always another dawn risen on mid-noon, and under every deep a lower deep opens” (Emerson, 1899). That being said, we must remember to “re-remember,” lest those trampled “schoolmasters” threaten to work their hands through the soil and grope for the sun.</p>
