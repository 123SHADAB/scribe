                    	<p>We are living in a time of transition. We observe the impact of inequality and greed on a daily basis. Our politics are more divided, inequalities based on income, race and gender persist, environmental degradation continues and we are now living with the disparate impacts of a global pandemic. With our world shifting to online platforms, we are becoming even more fast-paced and individualistic. As everything now revolves around a single user, it’s hard to take a second to pause and reflect. It’s even hard to lift up one’s head at dinner and step aside from the endless distractions of a world inside a screen.</p>
<p>
F. Scott Fitzgerald’s The Great Gatsby depicts a world in transition similar to the one we are experiencing today. Gatsby’s life perfectly encapsulates this change from midwestern simplicity to a more individually focused, fast-paced life of conspicuous consumption. It is a world in which commerce and greed have displaced more traditional societal norms and values. Gatsby’s life reflects the possibilities of social mobility, but also a world filled with corruption and new wealth disparities. It is a world in which the wealthy move fast and are so self-absorbed that they don’t take the time to reflect on their actions. The upheaval Fitzgerald portrays in Gatsby’s world creates a divided society, much as we see today, of the haves and have nots. In the shadows of Gatsby’s world of opulence and consumption, lies the Valley of Ashes, a wasteland on the highway between New York and Long Island overseen by the billboard advertisement of Doctor T.J. Eckleburg. This wasteland is characterized by Fitzgerald as a “solemn dumping ground” (24) and represents what happens to the working class and poor in a society propelled by decadence and greed. Furthermore, the Valley of Ashes is symbolic of the fall from innocence, similar to the one we are experiencing today under the Trump administration. There are striking similarities between Gatsby’s world and today in this transition from more democratic and inclusive values to a focus on materialism and the individual, oftentimes at the expense of the less privileged.</p>
<p>
Gatsby’s world is filled with great opulence, but also wealth disparities. Fitzgerald illustrates these disparities by contrasting the extravagant life of West-Egg Long Island with the Valley of Ashes, a place “where ashes grow like wheat into ridges and hills” (23). As compared to Gatsby’s house, “a colossal affair by any standard” modeled after “some Hotel De Ville in Normandy” (5), the Valley of Ashes is described by Fitzgerald as a place where ashes take the “forms of houses” (23). Even the road “shrinks away” from this “desolate” (23) area of land. Furthermore, it is a place where the worlds of the haves and have nots collide, highlighting the tension created by wealth disparities. As characters such as Tom and Gatsby come into contact with Myrtle and Wilson, the tensions between these two distinct groups of society are exposed. It becomes clear that wealth disparity creates an even greater power disparity. This is evident as Tom toys with Wilson by promising to sell him a car. Tom’s is a hollow promise, used to manipulate Wilson, highlighting Tom’s comparative social standing and the ease with which wealth can be used as a tool of manipulation. Similarly, Gatsby speeds through the Valley of Ashes in his yellow coupé, contrasting the luxury and opulence of his shiny new car with the “impenetrable cloud” (23) of ashes that hangs over the Valley. It is here, in the Valley of Ashes, that Fitzgerald contrasts these worlds and illustrates the rapid change brought by the increasing wealth gap in 1920s America.</p>
<p>
In the Valley of Ashes, this collision between two competing Americas is symbolically represented by Fiztgerald in the oversized billboard of Doctor Eckleburg. This enormous advertisement displays only the Doctor’s eyes which are described as “blue and gigantic – their irises are one yard high” (23), looking “out of no face” (23). Doctor Eckleburg’s eyes look down at the cars speeding through the Valley of Ashes, evoking the image of a God who has been forgotten and cast aside. When Wilson looks at the billboard he observes, “God sees everything” (160), signifying that even though God has been exiled to the Valley of Ashes he continues to watch and judge. The image of Doctor Eckleburg takes on significant metaphoric purpose in illustrating the abandonment of God in the chase for wealth and pleasure. The narrator observes that Doctor Eckleburg has been “forgot[en]” (24) and that his “eyes [have] dimmed a little by many paintless days, under sun and rain”(24), suggesting that God has been forgotten in this small, coal mining town. The Valley of Ashes itself is described as a “dumping ground” (24) and its characters represent the underbelly of society. It is the place where Tom cheats on his wife, Wilson learns his wife is cheating on him, and Daisy recklessly runs over Myrtle with her car. In this new age, Fitzgerald symbolically depicts the deterioration and abandonment of God through the powerlessness of Doctor Eckleburg to help the people of the valley.</p>
<p>
Fitzgerald illustrates the duality of this new American society through the inequitable ways industrialism favors the rich over the poor. The contrasts present between New York City and the Valley of Ashes represents this duality. As Nick approaches the City, he remarks that the view from the Queensboro Bridge is “always the city seen for the first time, in its first wild promise of all the mystery and the beauty in the world” (68), suggesting the endless possibilities of life in New York. In Nick’s initiation to the City he sees it as a place where “anything can happen” (69). Yet, as Nick spends more time in New York he begins to witness the other side of the American dream. His disillusionment comes in small steps as he pieces together the corruption of Gatsby’s side business with Wolfsheim and observes the unscrupulous favoritism of a policeman who looks the other way as Gatsby recklessly speeds down the highway. In the Valley of Ashes, Nick observes the environmental hazards of poverty as he sees the “impenetrable cloud” (23) of smoke from the coal mine polluting the air of the Valley yet propelling the commercialism of the City. Through Nick’s observations, Fitzgerald begins to reveal the duality of life in America where the wealthy build their lives on the backs of the poor and God is relegated to a commercial billboard on the side of the highway.</p>

<p>In the middle of these disparities on the road between New York City and West-Egg, Fitzgerald imagines a billboard that is both forgotten but watchful. Doctor Eckleburg presides over this middle ground as a kind of reminder, asking the characters to both slow down and reflect on their actions and impact on others. While driving, Nick observes that there is always a “halt of at least a minute” (24) in the Valley of Ashes, but if the bridge of the waterway goes up, there can be as long as an “hour wait” (24). This wait signals the need of the wealthy Long Island commuters to slow down and compare the excess of their lives with the poverty within the Valley. However, as characters like Tom and Gatsby drive through the Valley they notice nothing, driving even faster. In fact, it is Daisy’s inability to slow down when passing through the Valley of Ashes on her way to New York City which leads to Myrtle's death. Fitzgerald suggests that it is this extreme self-absorption and inability to slow down which is the root of so many problems in Gatsby’s world.</p>

<p>The parallels are easily made between the speedway of wealth in Gatsby’s world with the fast paced self-absorption of the wealthiest Americans today. A song lyric from the novel aptly observes, “<em>One thing’s sure and one nothing’s surer // The rich get richer and the poor get — children</em>” ( 95). Clearly, little has changed from Gatsby’s time to today as income inequality in the United States has reached its highest peak since Fitzgerald’s period of the 1920s. The novel suggests the problem is greater than mere disparity of wealth and goes to an attitude of selfish absorption that is a consequence of consumer culture. Today, as in <em>The Great Gatsby</em>, we distract ourselves by chasing material wealth, and hide the consequences of our consumption in the slums we quickly drive through and factories in developing countries with workers we will never see. Most of us turn a blind eye to the smog in our sky, the litter washed ashore on our beaches and tshirt weather in the dead of winter. Few of us can even look up above our screens at dinner to acknowledge those around us. We are so caught up in ourselves that we don’t stop to acknowledge our own Valley of Ashes in the detention centers for immigrants, the melting ice caps or the suffering caused by centuries of racism. What would the eyes of Doctor Eckleburg see today from a billboard in our Valleys of Ashes?</p>
