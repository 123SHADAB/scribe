                	<div class="text-details">
                    	<p>Born near the cusp of the 20th century, Millay was a fiercely independent, feminist poet who refused to  yield  to  societal  norms.  After  her  parents separated in 1904, Millay’s mother raised her and her  two  sisters  in  an  all-female  household,  encouraging  them  to  voice  their  ideas  and  hold  their  own beliefs. Millay did not take this advice lightly, getting in trouble with school authorities for her frank attitude  and  ultimately  deciding  to  pursue  a  liberal  education  at  the  all  women’s  college,  Vassar.  While  earning  her  degree,  Millay  began  to  perfect  her  poetry  and  explore  her  sexuality,  having several  short-lived  romantic  relationships with both men and women. Known as being particularly  promiscuous by her classmates, Millay avoided committing to serious relationships for the duration of her youth and touches upon her feelings after finally settling down in her poem “If I should learn, in  some  quite  casual  way.”  Through  the  progression  of  the  quatrains,  Millay’s  poem  “If  I  should  learn, in some quite casual way” conveys to the reader that, despite outwardly denying her love for her partner, in reality, she cares for him.  </p>
<p>

The sonnet’s opening quatrain consists of Millay attempting to show how trivial her current lover is to  her  while, simultaneously, undercutting her point with the structure and method she chooses to  express  her  message.  The  poem  begins  with  Millay  fabricating  the  hypothetical  scenario  of  her lover’s death and restating the poem's title “If I should learn, in some quite casual way” (Millay, Line 1).  By  establishing  her  partner’s  death  as only a hypothetical scenario with the phrase “If I should  learn,” Millay is able to distance herself from confronting the actual effects her lover’s death would have on her. After distancing herself, she attempts to trivialize him by suggesting she would learn of his  death  “in  some  quite  casual  way/...  Read  from  the  back-page  of  a  paper,  say,/  Held  by  a  neighbor  in  a  subway train” (Millay, Lines 1-4). By placing herself learning of her lover’s death off “the  back-page  of  a  paper...  in  a  subway,” she not only suggests his whole life was only one small  part  of  her journey somewhere else, but others – such as the man reading the paper – do not care about  his  death;  his  death  is  only  worthy  of  filling  the  “back-page  of  a  paper.”  Yet,  the  broken  sonnet  Millay  utilizes  undermines  her  argument  that  she  feels  indifferent  toward  her  partner.  A sonnet  consists  of  fourteen  lines,  each  containing  ten  syllables  and  written  in  iambic  pentameter;  Millay  breaks  the  traditional  form  by  concluding her first line with an anapestic foot in the phrase “casual way.” The fact that the anapestic foot in “casual way” is forced into the sonnet’s underlying meter shows that the indifference Millay says she has for her partner is difficult to say because it is inconsistent  with  her  underlying  emotions.  Millay’s  first  line  also  exceeds  the  ten-syllable  length  sonnet  lines  have  and,  by  failing  to  fulfill  another  fundamental  requirement  of  a  sonnet,  further suggests that her apathetic statements are inaccurate. The text continues to break the sonnet’s form in  the  first  quatrain  by  using  an  apparent  rhyme  between  the second and fourth lines, which read  “That you were gone, not to return again–/... Held by a neighbor in a subway train.” The apparent rhyme not only breaks the sonnet’s structure, but shows her thoughts linger on her partner’s death as  she  writes  “That  you  were  gone,  not  to  return  again.”  The  phrase  “not  to  return  again”  also  suggests  that  she  will  miss  his  “return”  and  shows  that  she  does  feel  for  him.  Millay  attempts  to deceive  the  reader and, in doing so, creates a microcosm of what her text does with her emotions:  directly  states  a  lack  of  emotion  while  subtly  conveying  through  diction  and  structure  that  in actuality she has great affection for him.  </p>
<p>
The  second  and  third  quatrains  reinforce  the  pattern  set in the opening four lines: first distancing  Millay from her partner to enable her to show apathy for him and then revealing how she in reality cares  for  him.  The  first  lines  of the second quatrain begin with Millay continuing her hypothetical  scenario,  stating  “How  at  the  corner  of  this  avenue/  And  such  a street (so are the papers filled)” (Millay, Lines 5-6). The vague terminology of “this avenue and such a street” enables Millay to not directly  confront  the  emotions  she  has  and  deny  the  fact  that  her affection for her partner exists.  Ambiguous  diction  is  further  used  in  the  following  lines,  which  states  “A  hurrying  man  –  who happened  to  be  you  –/  At  noon  to-day  had  happened  to  be  killed”  (Millay,  Lines  7-8).  The  text  touches upon the incident that killed Millay’s lover, yet does not use his name and simply calls him “A  hurrying  man – who happened to be you,” allowing her to distance herself from the event and  discuss  it  less emotionally. Despite the denial Millay’s poem voices in the text’s first two quatrains, the  poem’s  third  quatrain  and  final  couplet  confirms  that  the  author  cares deeply for her partner.  The final quatrain begins with Millay stating after reading the news that “I should not cry aloud – I could not cry/ Aloud, or wring my hands in such a place” (Millay, Lines 9-10). Although beginning with an attempt to conceal her true emotions, “I should not cry aloud,” she quickly fails and the true reason  why  she  does  not  “cry  aloud”  is  stated:  “could  not  cry/  Aloud...  in  such  a  place.”  Millay  implies that, although she wishes she could “wring [her] hands” and “cry aloud,” she is unable to do so  since  she  is  “in  a  subway  train.”  The  poem  concludes  with  the  lines  “I  should  but  watch  the  station  lights  rush  by/  With  a  more  careful  interest  on  my  face,/  Or  raise my eyes and read with greater care/ Where to store furs and how to treat the hair” (Millay, lines 11-14). By beginning with “I should,” Millay implies that the proper thing to do would be to act as if she did not care at all and simply “watch the station lights.” Yet, since Millay was never one to adhere to societal expectations, she  likely  writes  of doing trivial things such as reading “with greater care/ Where to store furs” to  speak to how ridiculous denying her emotions would be.  </p>
<p>
Millay’s  poem  “If  I  should  learn,  in some quite casual way” takes the traditional sonnet – a poetic  form  used  countless  times  throughout European history to express love – and contorts it to allow the structure to aid in communicating her complex feelings in a modern era. Therefore her work is not only an exemplar of modernist poetry but a wonderful window into who Millay as well. Rarely yielding  to  societal  expectations,  even  in  the  face  of  hundreds of years of European tradition, she  breaks the sonnet to announce her profession of love exactly as she intended: with her own voice. It is  that  willingness  to  experiment  and  challenge  the  accepted  that  allows  poets  and their poetry to  consistently puncture fallacies and portray every human emotion precisely. Edna St. Vincent Millay, having  articulated  the  ineffable,  has  solidified  her  legacy  as  one  of  the  great  American  modernist  poets and a woman who will forever speak her mind through her work. </p>

