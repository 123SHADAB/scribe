                	<div class="text-details">
                    	<p>In Sophocles’ Oedipus the King, Oedipus is warned of a plague summoned by the wrath of the gods. With the loving support of the people, Oedipus is implored to act as the city of Thebes’ “greatest man” (Sophocles, l.54). Immediately, Sophocles illustrates Oedipus as a likable and encouraging leader. From these qualities Oedipus was made King and given a luxurious life. However, as the story progresses, Oedipus’s life unravels – transforming from a once ignorant existence to a grotesque and ironic truth. The Greek poet, Sappho, reflects on a similar humanistic philosophy, despite (or in spite of) the bitter-sweet timbre used to describe the love she once had. Poem 16 expresses the value of an intimate relationship, rather than the pride of parading soldiers. Her celebration of love and the human condition transforms into a grim and melancholic existence. Sappho and Sophocles’ work share a striking similarity of transformation; from a charming and noble reflection of love and leadership, to a synopsis of life’s final outcome as an utterly bleak, and consuming void, with the only means of survival being the retreat into one’s own mind.</p>
<p>
Sophocles describes Oedipus as a father to his people. When the Priest comes to him with news of the plague, Oedipus explains that he is aware of his city’s malady and consults Creon and the Chorus to “cleanse ourselves” (Sophocles, l.112). Oedipus views his citizens as kin, “My children” (Sophocles, l.1). When Oedipus learns the city’s misfortune is due to the murder of Laios, he springs into action, eager to see justice and balance restored: “Apollo was exactly right, and so were you [Priest], to turn our minds back to the murdered man. It’s time I joined your search for vengeance; our country and the god deserve no less.” (Sophocles, l.153-156). Oedipus is willing to join the search of the killer for the sake of a harmonious community. During the opening of the poem, Sophocles demonstrates Oedipus’s desire to be an honest, noble leader.</p>
<p>
As the poem continues, Oedipus’ transformation is slowly teased out. As he meets with Tiresias and Creon, Oedipus becomes suspicious and rash. The once benevolent leader, devoted to his people begins to shift towards more selfish desires: “wealth and a king’s power, the skill that wins every time– how much envy, what malice they provoke!” (Sophocles, l.457-459). Eventually Oedipus speaks with the herdsman and comes to the truth that he caused the “great grief to my people” (Sophocles, l.1539). Under towering guilt, Oedipus implodes. He gouges out his eyes, “I’d deaden my whole body, go blind and deaf to shut those evils out” (Sophocles, l.1573-1574), and wishes for the termination of all his senses. Originally, Oedipus was the righteous, civil servant to his people, however when his truth is realized, his entire life becomes a lie. Sophocles creates a king who was protective and noble, only to be broken by the will of the gods. In a final act of desperation, Oedipus pleads with Creon to let him be banished and to “a place where no living person will find me” (Sophocles, l.1629-1630). Oedipus’s shame is so overwhelming he can no longer bear to be around other people. Oedipus’ story combined with Sophocles’ closing statement: “Don’t claim any man is god’s friend until he has passed through life and crossed the border into death– never having been god’s victim.” (Sophocles, l.1743- 1746), edifies the enduring lesson that no man is at the mercy of the gods until he his dead. Sophocles’ blanket statement, from the voice of the omniscient lower class, translates as follows: those who are not “god’s friend[s]” will end up similar to Oedipus – tragically alone and condemned to spend the rest of their lives in a wallowing, destitute emptiness.</p>
<p>
Although Oedipus is a far more dramatic embodiment of the transformation from the celebration of life to solitude, Sappho’s poetry also contains this similar complex. In her works, Poem 16, Fragment 48, Poem 94, and Fragment 102, Sappho portrays herself as a deep, romantic thinker. In Poem 16, Sappho compares the rush of emotions her peers feel when they see the “chariots of Lydians or ranks of footsoldiers in arms” (Sappho, l.19-20) to when she thinks of her girlfriend, Anaktoria. Poem 16 is the embodiment of Sappho’s vibrance and passion: “I would rather see her lovely step and the motion of light on her face...” (Sappho, l.17-18). Similarly, in Fragment 48, Sappho describes her seemingly undying flame of love, “you came and I was crazy for you and you cooled my mind that burned with longing” (Sappho, l.36-37). These two poems both exhibit a desire to express the love and euphoria that comes from the connection with another person. Despite this original romanticism, a tangible transformation begins to appear in Poem 94. Instead of celebrating Anaktoria, Sappho mourns the loss of a loved one. Again, Sappho reveals her intense emotions, “I simply want to be dead. Weeping she left me with many tears...” (Sappho, l.1-3), granted this time she is significantly more remorseful. In Fragment 102, Sappho’s remorse continues to manifest, “sweet mother I cannot work the loom I am broken with longing for a boy by slender Aphrodite”. It is in Poem 168B, where Sappho’s transformation is unmistakable Once the vibrant, and wondrous romantic, Sappho’s passion decayed into a suffering silence: “Moon has set/and Pleiades: middle/night, the hour goes by,/alone I lie”. Living in the solace of the night, Poem 168B shows the once amorous and introspective poet transfixed on burning the night away. During each individual poem, a transformation occurs in Sappho’s writing – eventually resulting in an ephemeral overture of the defeated romantic, declaring her soul desire: to exist in utter solitude.</p>
<p>
Sappho and Sophocles’ literary work begin by offering a relatively innocent interpreta- tion of life, but as their narratives progress, both Oedipus and Sappho suffer tremendously. As Sappho and Sophocles reveal their tragedies, the two poets conclude that the only means of surviving suffering is when one recedes from society and into the mind. Some critics may argue the intent of Sophocles and Sappho’s work; that if there is zero intrinsic meaning to life, then why create literature at all? Why must the reader struggle along with Sappho and Oedipus if the desired conclusion is universal abandonment? Perhaps it is that Sappho and Sophocles recognize that all suffering is inevitable, but that one should not squander life grieving this truth. Perhaps it is worthwhile to savor the delicacies of life while they remain, and once they do depart, understand that you were born to bow your head to solitude.</p>
