                    	<p>This poem by Billy Collins is both outwardly and inwardly playful; at first glance, it is a celebration of being
briefly carefree, almost sing-song at times, that ends in the world of the children, where everything is smaller
but feels bigger. It is also a series of reactions by the speaker which end in his embracing not just the snow
itself, but the idea of letting a sort of chaos reign, if only for a day, if only compelled by one’s own childish
curiousity to see just what might happen. Collins’ speaker feels the gravity of such an ostensibly insignificant
day, and as winter whiteness settles over what seems to be the whole universe, he sets out to see what world
fills in this blankness—and discovers a kingdom.</p>


<p>The poem is cast first through the lens of childhood when we read the title: “Snow Day.” Any child who
grew up in the United States, where the poem is likely set—Billy Collins is from New York—knows the giddy
anticipation of waiting for the public school system to announce a snow day on the radio. To adults, a snow
means cleaning slush from the house steps and taking wearisome drives on icy roads, but to children, a snow
day means freedom. Tussling with brothers and sisters in warm coats, diving into fluffy mountains, coming
back into the delicious warmth with faces reddened and hands frozen. So when we hear the word
“revolution” in the first line, it is a riotous promise of all these things, and the snow day itself becomes a
metaphor. The revolution is against the opposite of a snow day: an early August first-day-of-school, perhaps,
where children walk to campus in groups, kicking their heels against the pavement and chatting idly.</p>
<p>
Now, one wouldn’t expect such an electric and joyous uprising to be announced with a white flag, which we
generally associate with surrendering one’s cause, but in fact, the white flag is only a symbol of truce,
ceasefire, or intent to negotiate. It implies surrender when the weaker party carries it, but what can stop snow?
Especially on this day, when the whole “landscape” is “vanished” beneath its conquering drifts, and the world
is so white that not a single black spot, small as “a mouse,” interrupts the waves of blankness, nature has the
upper hand. But the snow, like the white flag, symbolizes peace: perhaps the regular rhythms of life that it
overwhelms are less dangerous than whatever it seeks to bring.</p>
<p>Our speaker is a young man, likely single, and certainly not a father, as there is no mention of his own child
taking to the blanketed streets. It’s mid-morning, the snow day has just been announced, and he is peering
out of his windows—behind which he stands like a medival peasant watching enemies storm the
castle—examining the spectacle. He has a sense of humor or at least lightheartedness about the situation: he
plays with the words he uses to describe it, evident by the subtle assonance in “govern-” and “smothered”
and the slant rhyme in “libraries” and “buried.” Even still, he knows the impact this weather will have on the
goings-on of society. The buildings that house the beauracracy and the buildings that train children to serve it
aren’t just obscured or covered by the snow, but suffocated, stifled, put underground; they have been
effectively, figuratively, killed by the snow. This is echoed too in the word “fallen.” The world may really only
be “softly blocked,” paused for a short time, but in the image of the total whiteness around, it feels as though
the the regular world is gone forever. And what world is that? One where the “trains” cause strife and anxiety
simply by failing to transport their patrons to their offices, the shiny buildings where they keep the cogs of the
market turning. With the “post office[s]” shut down, tax invoices will go undelivered, an anxious student will
not receive their college acceptance letter, and somebody’s resume won’t make it to that law firm downtown.
The beauracracy in the “government buildings,” so hindered, will fall behind on filing traffic court cases;
those surveys about the mayor’s approval ratings will have to be looked at tomorrow. This is the world of
workaday America, of civil society and adults’ watered-down dreams. The tone of these associations is
“grayed in, and gray,” as Gwendolyn Brooks says in “kitchenette building,” her own meditation—though
different in many ways—on a life constricted. But this flat world and its false small talk, its transactional
bickering, are silenced by the “noiseless drifts” of the snow, the sky’s rephrasing of a child’s petty shut up!
Before the children can pick up where they left off on the playground, the status quo must be overthrown,
and indeed, it appears a coup has been staged.</p>
<p>At this point, before moving on, we break focus and consider a moment the speaker anticipates having:
“step[ping] out” into the snow and making himself a part of the scene. Since this poem dedicates such focus
to the snow and the schoolchildren, it is curious that we take a full stanza to look at a walk that the speaker
hasn’t even taken yet, a glimpse into the future. Even more curious is how in a poem about snow, much of
the third stanza uses water as a metaphor. The speaker moves like he is “walking” in it, and his dog even
“porpoise[s]” along. Water, of course, is melted snow, so we can assume that at this point, the revolution is
over. But when one walks in water, it is a slow, clumsy process, and such as it must be with the speaker. He
must drag his feet along, held back somehow; he probably can’t see much of the ground he’s stepping on, and
his dog is a great help to guide him along. Even though the procedures of the adult world have presumably
been reinstated, the speaker is having trouble adjusting back to them, and he experiences what children must
feel as they’re gradually exposed to “the real world.” This is the key to why this speaker is the one telling us
this story: his soul, too, lives still in the world of snow, though his physical adult body lives with everyone
else. Even when confronted with the aftermath of the snowy revolution, he tries to walk through water, when
it might be much faster and less tedious to skip through the deep puddles or even to swim, as a five-year-old
might certainly attempt, and as his free-spirited puppy is doing. No, he still resists, tries to tread pedantically
to wherever he is going. The fresh memories of the riotous snow day are little “shower[s]” that can be
“shake[n]” from the trees, but no longer form the ground he gets to stand on.</p>
<p>“For now,” though, the speaker is currently still cozied up in his home, happily consigned but not passive: he
is aware that his being “willing” to stay trapped by the circumstances is a political choice. He has sided with
the snow, whatever it brings. It is here that we take a turn and a tone shift occurs as we first actually see the
children alluded to by the title and situation. The “anyone” hearing the news about schools closing is, in every
case, going to be a child. The following two stanzas can be interpreted in multiple ways: on one level, they are
the announcements of the news anchor, unaffectedly listing off each school, and, with each name, eliciting
eruptive cheers in households around the city. On another level, the speaker is poking fun at the schools
themselves. Their names are mostly plausible, but—especially knowing that the speaker is siding against the
schools today—we can take a few of them (“the Ding Dong School... Peas-and-Carrots Day School”) as
mockery. Even further, however, if we do assume that all the school names are real, we see how
condescending they are towards children. Children are sent to school not to learn, but to be beckoned “all
aboard” on false journeys of play, taught only to follow the leader and play Simon says. They are equivalent to
cute little “toadstool[s]” cropping up in circles in the garden like round buttons. Their activities are “peanuts,”
in the idiomatic sense: totally insignificant compared to the lives of adults. The innocent names of
pre-schools, too, are not just targets of the revolution, but part of the very reason a revolution must exist at
all.</p>
<p>And in a moment, this occurs to the speaker. These places that he mocks house not “sparrows,” not even
“stars,” but people, naive ones, but more free. The speaker can see them clearly, suddenly, racing about,
emerging from their hiding briefly during recess and without abandon today. This is an emergence that will
occur less and less as the children grow older, when the snow melts and then comes anew and they do not get
to do the same. Struck, perhaps, by the ephemerality of these moments, the speaker listens “hard.” The
schoolgirls who catch his attention, normally confined to “the fence,” as far away from their school building
as they can get, are, today, free. It is now that we know for sure who this revolution is really for. The snow is
not fighting on behalf of itself. It is schoolchildren versus school and the future it represents, that age-old,
pesky rivalry, but when the speaker buys into it, it becomes more than that. It’s almost as if the “plotting” of
the schoolgirls made the snow a necessity; perhaps nature bent to one of these “small queen[s].” A “riot,”
literally, is when a crowd (the children streaming from their homes, not to school, but to fields and
playgrounds) disrupts the peace, causes an outburst, an uproar. And though these kings and queens are s mall,
and naive, and likely will grow up to be sensible, pragmatic adults—in this temporary reality created by the
snow, the dramas acted on the stage of kindergarten-hood are elevated. School is out; who’s to tell them to
stop playing pretend, or that their games are pretend at all? In the absence of adult tragedies to distract from
them, they are as “grandiose” as any royal coup d’etat. And the speaker is compelled by them. So before
“stepping out,” attempting to feel the snow and breathe the air and see the world once again, the speaker
holds his breath and lets the young monarchs reign. Letters are lost, libraries full of “real” history are several
feet under, and these children, who will one day shape history themselves, get their practice in earth-shaking.
Just as history’s favor swings back and forth between powers in an endless pendulum motion, so too does
this small history, this proxy history, and today, the favour is with the children. In Emily Dickinson’s “Safe in
their Alabaster Chambers,” “Diadems - drop / Soundless as Dots / On a Disk of Snow.” No matter how
small the children’s crowns may be, the speaker of Collins’ poem knows that the true revolution, the
rising-up—in the midst of so much forced raising-up—is that for a day, they get to put the weight of those
diadems on their own heads.</p>

