                    	<p>In Toni Morrison’s novel A Mercy, a vivid, evocative account of colonial America in the late seventeenth
century, Jacob Vaark represents the archetypal American individual. One of many colonists at the time
attempting to create a life for himself in the unstable New World, his ascent from orphan to landowner is a
quintessential American success story. However, Jacob is also one of Morrison’s most ironic characters,
whose climb up the social hierarchy is accompanied by a departure from reason and morality. By depicting
Jacob’s journey as one of self-destruction, Morrison parodies traditional literary epics and, more specifically,
American origin mythologies. In doing so, she critiques America’s hubris, calling for a reckoning on the
values of individuality and material prosperity that form the pillars of the American Dream.</p>


<p>Morrison’s ironic treatment of Jacob as the “American Adam”—a literary trope conceptualized by scholar
R.W.B. Lewis—aims to condemn the romanticization of America’s founding. As Jacob rides through the
Chesapeake, the narrator illustrates his surroundings using biblical echoes: “Breathing the air of a world so
new, almost alarming in rawness and temptation, never failed to invigorate [Jacob]. Once beyond the warm
gold of the bay, he saw forests untouched since Noah, shorelines beautiful enough to bring tears, wild food
for the taking...” (14). Morrison consciously invokes the Garden of Eden through describing early America as
a breathtaking, “untouched” region brimming with “temptation.” Her allusion to the biblical figure Noah,
who sailed to a new land to restart the human race, conveys the profound rawness of the country that Jacob
has entered, presenting the land as having an innocence of its own. The narrator’s portrayal of Jacob as a man
of great independence, who engages in heroic acts such as rescuing wounded animals, represents him as the
Adam inhabiting this new Eden. However, rather than glorify early America and its first inhabitants,
Morrison’s narration is drenched in irony that reveals the fundamental impurity that America was founded
upon. Jacob, while seemingly moral, fuels slavery through his commerical pursuits, despite his attempts to
convince himself that “his own industry could amass the fortune, the station, D’Ortega claimed without
trading his conscience for coin” (32). Ironically, “trade his conscience for coin” is exactly what Jacob does
through supporting the slave trade. Morrison presents Jacob’s self-delusion as reflective of America’s
tendency to perceive itself as exceptionally moral, while ignoring the evils that are at the heart of its founding.
Furthermore, while the American Adam is described in literature as entirely independent and self-sufficient,
Morrison disrupts this myth by revealing how Jacob’s independence is inseparable from a dependence on the
unfree, whether it be Willard and Scully, Rebekkah, or the enslaved people on his plantation. Ultimately,
Morrison’s representation of Jacob as the American Adam is a parody, one that reveals the moral rot that
festers under the Edenic facade of colonial America.</p>


<p>Through her exploration of different forms of individuality on the Vaark patroonship, Morrison interrogates
the flaws of American individualism. Lina, whose Native American culture has instilled her with a unique
appreciation of community, reflects on the dangers on Jacob and Rebekkah’s mentalities: “Sir and Mistress
believed they could have honest, free-thinking lives, yet without heirs, all their work meant less than a
swallow’s nest. Their drift away from others produced a selfish privacy...Pride, [Lina] thought. Pride alone
made them think they needed only themselves, could shape life that way...” (69). While American culture
praises the individual and their “private,” self-motivated scramble to the top, Morrison expresses a different
sentiment through Lina’s reflection: to solely focus on one’s own needs, as Jacob does, is an act of “pride”
that leads to a myopic, delusional existence. By defining Jacob’s obsession with individualism as his fatal flaw,
Morrison condemns the way in which a prioritization of “privacy” and material prosperity shoves aside
spiritual values of love and generosity. In contrast to Jacob’s “selfish privacy,” Lina and Sorrow reflect
different, healthier understandings of individuality. Both develop their identities and learn to respect
themselves through the relationships that they form with others: Sorrow becomes symbolically “Complete”
by raising her child, and Lina gains a sense of self-worth through her efforts to protect Florens. In her
demonstration of these differences between Lina, Sorrow, and Jacob, Morrison underscores the power of
community and the value of forming authentic human connections.</p>
<p>In her depiction of Jacob’s “Fall” and the highly symbolic house that he leaves in his wake, Morrison rebukes
American exceptionalism, refocusing America’s history on its oppression of the unfree. After Jacob dies from
smallpox, Rebekkah and the other women fulfill his dying wish by bringing him to his empty mansion, “laying
him in the mud” in order to unbolt the door (105). Although Jacob certainly has obtained “success” as
defined by the American Dream—property and social mobility—his pursuit of material prosperity has led
him to sacrifice his integrity. By giving Jacob a lack of dignity in death, where the mud he lies in implies his
“muddied” conscience, Morrison parodies heroic portrayals of traditional epic protagonists. Often in
literature, epic heros culminate their quests by reaching an achievement that is central to the traditions and
beliefs of their culture (Notes and Quotes). In A Mercy, the house that Jacob builds is a reversal of this aspect
of an epic: rather than a cultural “achievement,” the house is a testament to the evils embedded in the fabrics
of America, in which the serpent gate symolizes the sin of slavery. Through describing Jacob’s mansion as a
“grand house of many rooms rising on a hill above the fog,” Morrison parodies and critiques John
Winthrop’s infamous “city on a hill” metaphor asserting American exceptionalism. If Jacob’s house
represents America, then it is significant that it was constructed by a “crew of [unfree] laborers:” America
was, indeed, built and developed through the labors of the unfree (175). Willard, one of the workers who
assisted in the building of the house, describes how “neatly [the] iron bars led to the gate, each side of which
was crowned by a flourish of thick vines. Or so he thought. Looking more closely, he saw the gilded vines
were actually serpents...” (132). Just as the serpents appear at first glance to be exquisite, “gilded vines,”
Morrison implies that America is a master of deceit and equivocation, a nation that uses a facade of
exceptionalism to hide the sins that lurk beneath. Furthermore, the fact that each character in A Mercy
describes the house and its serpent gate differently—Lina believes that the house is “sinister,” while Willard
and Scully view it as awe-inspiring—speaks to the complex variety of perspectives through which America is
and always has been viewed.</p>
<p>Especially in today’s polarized political climate, there are many who vehemently oppose critiques of America.
Just this past week, the White House denounced a suggestion to integrate the New York Times’
groundbreaking “1619 Project” into California public school curriculums, claiming that “no American student
should be made to feel ashamed of their history or identity.” Quotes like these are almost Orwellian,
proposing to erase essential aspects of American history because they do not fit the bright, “city on a hill”
idealizations of America that many politicians prefer. As Morrison expresses throughout A Mercy, engaging in
uncomfortable analyses of American history and extracting the flaws from American mythologies are the sole
way that we as a country can achieve true national growth and enlightenment. Reexamining American history
and values from a critical lens is only the first step, however: it is also the responsibility of every American to
actively devote themselves to dismantling American systems of oppression. In a way, Jacob Vaark’s
self-delusion mimics the privileged Americans today who continue to remain blind to the ways in which they
benefit from those systems.</p>

