                    	<p>In both the fictional realm and the real world, the atmosphere of a country mirrors the characteristics and
mentality of its ruler. In William Shakespeare’s Macbeth, after Macbeth kills Duncan, the former, corrupted by
his obsession with his prophecy and new position as king, begins to spread an infectious disease all
throughout Scotland; as Scotland is plagued by death, sickness, and chaos, Lady Macbeth and Macbeth leave
themselves vulnerable to the disease, which causes the deterioration of their mentalities and loss of Macbeth’s
high morals. In the climactic acts of 4 and 5, the Macbeths are the sources of sickness that spread to all of
Scotland, leaving the country in a chaotic, painful state; the combination of the Macbeths’ deaths and
Malcolm’s ascent to the throne is the antidote and the source of healing for Scotland.</p>


<p>To begin, Lady Macbeth is the cause of the illness which will soon spread to Scotland, her husband, and
herself. In Act 1, strong-willed Lady Macbeth persuades Macbeth to murder Duncan, insulting Macbeth’s
manliness by calling him a “coward” (I.vii.46). Macbeth resolves to kill Duncan, provoked by Lady Macbeth’s
insinuations about his character, and his murder originates the illness and furthers the spreading of it. In Act
2, after Macbeth kills Duncan and is shaken to his core by realizing the consequences of his actions,
level-headed, rational Lady Macbeth attempts to calm down her husband by assuring him that “A little water
clears [them] of this deed” (II.ii.80). Ironically, over the course of the play and with the help of Macbeth’s
own displays of regret, Lady Macbeth realizes that this murder cannot be undone. As the crushing guilt of her
actions suffocate Lady Macbeth (on and off-stage), the results of Lady Macbeth’s illness are depicted in the
final act of the play as Lady Macbeth is unable to sleep peacefully, experiencing “unnatural troubles” (V.i.63)
due to her “Unnatural deeds” (V.i.64). In Act 5, Lady Macbeth experiences hallucinations of imaginary
blood–—Duncan’s blood—on her hands while sleepwalking, a “great perturbation in nature” (V.i.8), and
relives the night of Duncan’s murder. “Out, damnéd spot! Out, I say!” (V.i.31) She yells while rubbing her
hands together as if she were washing them. The doctor notes that “[t]his disease is beyond [his] practice”
(V.i.51). Lady Macbeth is suffering from a moral, mental problem, in which she requires immediate help from
“the divine [rather] than the physician” (V.i.66). The only cure to Lady Macbeth’s internal illness is an
antidote that she “must minister to [her]self” (V.iii.49). Moreover, when talking to Macbeth, the doctor
announces that Lady Macbeth is not physically sick as “she is troubled with thick-coming fancies” (V.iii.43).
In an attempt to poison Macbeth to achieve his desires, Lady Macbeth has poisoned and “infected” herself as
well (V.i.64). To further illustrate that a moral sickness has engulfed Lady Macbeth, Macbeth refers to his
“disease”-minded (V.iii.46) wife as having a “stuffed bosom” (V.iii.50), full of “perilous” (V.iii.50) and
“rooted sorrows” (V.iii.47). The “rooted sorrow” that Macbeth refers to is the guilt embedded in Lady
Macbeth’s “sorely charged” (V.i.46) and a burdened heart, burdened with the secret that she participated in
murdering the king. In this way, Lady Macbeth is a victim of a detrimental, infectious, and spreading sickness
that she created by convincing and assisting Macbeth in murder.</p>
<p>
Secondly, King Macbeth, having killed Duncan, nurtures the disease and spreads it to the rest of his kingdom.
Simultaneously, as the illness develops in Macbeth, he becomes apathetic and indifferent to murder and
grows careless about human life, including his own. Throughout Act 1, Macbeth is a “brave,” merciless, and
honorable warrior in battle favored by the public (I.ii.18). In Act 2, after the murder, Macbeth, overcome with
shock, grief, and regret, has difficulty keeping himself together to prevent raising suspicion and confesses that
he is “afraid to think [of] what [he has] done” (II.ii.61). Macbeth notes that no ocean can clean his filthy,
bloody hands; instead, when he puts his hands in great Neptune’s ocean, his hand will “incarnadine” the sea,
“Making the green one red” (II.ii.74- 75). Macbeth articulates that his act of violence, signified by his bloody
hands, and the sickness it has caused will spread. Later, as the image of the bloodied ocean foreshadowed,
Macbeth does not give himself a moment to think rationally about his decisions but acts immediately on the
“very firstlings of [his] heart” (IV.i.163) by ordering the execution of Macduff’s family in Fife. Rather than
killing the innocent himself, Macbeth sends murderers to do the job for him, causing others to become
murderers themselves. Similarly, Macbeth has troops with him to fight Malcolm and Macduff’s forces.
However, the public’s opinion of Macbeth has changed since he has become king. Under Macbeth’s rule, his
servants and troops have “absent” (V.iv.18), sick, and infected hearts; they do not serve Macbeth out of love
or loyalty like a healthy servant-king companionship would entail but out of pressure, “constrainéd things,”
(V.iv.17) to obey Macbeth. When the opportunity arises, “both more and less have given him the revolt”
(V.iv.16). Additionally, the enemy side reports on the sickened–”mad” (V.ii.15)– Macbeth, who is plagued by
his “pestered senses” (V.ii.27). He can no longer return himself, “his distempered cause” (V.ii.17), or Scotland
to sanity by containing it within “the belt of rule” (V.ii.18). As Macbeth’s regression of character and morals
continues, Macbeth cockily kills the Young Siward while “smil[ing]” at his sword (V.viii.16). Macbeth is not
only apathetic to murder, but he feels joy in killing others. Furthermore, Macbeth sighs, admits that he has a
“sick heart” (V.iii.21), and says he has “lived long enough” (V.iii.24). Although he is not actively suicidal,
Macbeth does not care about his well-being. The king feels fatigued by the sun’s presence, which alerts him of
another miserable day of ruling over Scotland. More so, Macbeth wishes to see the “estate o’ th’ world”
(V.v.53) fall apart and be “undone” (V.v.53) for his own selfish entertainment; Macbeth is sick at heart,
displayed by his resignation to the wrecked state of things. All in all, the events, dialogues, and actions of
certain characters throughout Acts 4 and 5 illustrate the sickness Macbeth has been infected with and how he
has spread said illness throughout Scotland.</p>
<p>
Thirdly, although Scotland falls into chaos that seems irreversible, Malcolm and Macduff are the healing
agents to Scotland’s sickness; by causing the deaths of the Macbeths and planning to establish new laws over
time with honest intentions to cure Scotland, Malcolm and Macduff provide the country with a more hopeful
and joyous path in the future. Throughout the course of Macbeth’s reign as king, Scotland dissolves into a
“bleed[ing]” (IV.iii.37) and “grave[-like]” (IV.iii.187) country consisting of “howl[s]” (IV.iii.6) full of “dolor”
(IV.iii.9), daily “gash[es]” (IV.iii.47) and “wounds” (IV.iii.48), unacknowledged “sighs...groans, and shrieks
that rent the air” (IV.iii.189), “modern” (IV.iii.191) sorrows, and innocent, “sicken[ed],” (IV.iii.194) men.
Evidently, due to Macbeth’s selfishness, displayed when he demands the witches to answer him “even till
destruction sicken” (IV.i.60), and his decision to prioritize his prophecy before the country, Macbeth is
actively allowing Scotland to become diseased.</p>
<p>
However, Malcolm, who had his birthright stolen from him, and Macduff, who suffered the loss of his family,
decide to make “med’cines of [their] great revenge” (IV.iii.248) in order to “cure this deadly grief” (IV.iii.249)
caused by Macbeth. The king of England, bestowed with a “heavenly gift” (IV.iii.175) of a “healing
benediction” (IV.iii.174) and the ability to help even the most “pitiful to the eye” (IV.iii.169), aids Malcolm
and Macduff by providing them with troops. The implication that, because the God-blessed king is assisting
Malcolm and Macduff, God is essentially siding with Malcolm and Macduff further supports that these two
men are the antidote to the sickness. Additionally, Malcolm’s troops, “every drop of [them]” (V.ii.34), who
fight on the side of the “med’cine,” (V.ii.32) declare to “purge” (V.ii.33) Macbeth, the “sickly” (V.ii.32) king;
the troops plan to “dew the sovereign flower” (V.ii.36), meaning they will nurture the true monarch, Malcolm,
while “drown[ing and murdering] the weeds” (V.ii.36), which are Macbeth, the greedy plant that absorbs all
the water, leaving the country sick and suffering. Moreover, Macduff holds the “usurper’s curséd head”
(V.viii.66) on display and announces that Scotland is “free” (V.viii.66) from the evilness and illness that was
Macbeth. In agreement with Macduff, Malcolm ends the play on a hopeful and optimistic note of reforming
Scotland bit by bit as time passes: ”What’s more to do / Which would be planted newly with time”
(V.viii.76-77) and happily invites all of Scotland to his coronation. As Malcolm is the son of the gracious
Duncan, a righteous ruler, it is implied that Malcolm will follow in his father’s footsteps; Malcolm, alongside
Macduff, will be a part of Scotland’s remedy.</p>
<p>
In conclusion, Shakespeare expertly crafts a theme of sickness and health to convey that the Macbeths create
and foster an illness which spreads through the country, leaving Malcolm and Macduff to heal and
reconstruct Scotland. Although it is short, Malcolm’s optimistic final speech alludes to Scotland’s inevitable
restoration of health. By the same token, plant imagery in Act V such as “rooted sorrow” (V.iii.47), “dew the
sovereign flower” (V.ii.36), and “drown the weeds” (V.ii.36) articulate the same message. The “bad plants” or
”rooted” (V.iii.47) evildoers like Macbeth must be drowned given the threat that they pose; these wicked
plants can spread sickness through their roots, leaves, and pollen while establishing a strong foundation to
maintain the sickness for generations. However, the aforementioned two motifs contain an even deeper and
symbolic meaning about life, one that is still applicable to modern times: power, desire, and greed are a deadly
combination, leaving anyone, no matter how virtuous and principled, susceptible to corruption, psychological
disease, and/or physical illness.</p>

