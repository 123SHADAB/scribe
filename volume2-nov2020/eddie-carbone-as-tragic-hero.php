                    	<p>Arthur Miller’s A View from the Bridge attempts to incorporate themes and traditions that reflect the era in
which it was written in to create a modern and relevant Greek tragedy of an everyday man. The playwright
has therefore presented the central character, a hard-working longshoreman in Red Hook named Eddie
Carbone, as a ‘tragic hero’. Traditionally, a tragic hero possesses nobility and desirable qualities, however his
actions and behaviour throughout the play reveal his fatal flaw, ultimately leading to the reversal of his
fortune and his downfall. A View from the Bridge largely conforms to these standards, though centrally
challenges the quintessential ideals of Greek tragedies</p>
<p>Hamartia is present in all traditional Greek tragedies, therefore the notion of a fatal flaw (and consequently, a
downfall) is part of Eddie’s innate character in multiple ways. Explicitly, his fondness for Catherine
exemplifies his flaw. He refers to his niece as “Madonna”, exhibiting his interest. This allusion connotes
purity, innocence and uniqueness – in the playwright’s era, it also symbolised the Virgin Mary. In viewers of
the play, this primarily creates an unsettling feeling, as this conversation takes place in the beginning where
many, including the characters, do not understand the full extent of Eddie’s affection. Miller therefore
appropriately establishes hamartia and thus presages Eddie’s downfall. In connection with “Madonna”, the
protagonist frequently uses “heh” in a mocking tone, to suggest overconfidence and masculinity when talking
to Catherine, which is, in part, aided by her behaviour to Eddie due to his respected stature (explored in
further sections). Miller’s stage directions show Eddie’s behaviour overtly. When he is hugged ‘He is af ected by
her but smiles his emotion away.’ This once more reinforces his deep love as he is “af ected”, but also shows his
attempt to be masculine, therefore “smiles his emotion away”. Miller’s careful placement of stage directions
creates irony because Eddie’s emotions will be his downfall and “away” is only temporary, another act of
foreshadowing. Ultimately, the audience can infer, from the beginning, that Eddie’s relationship with
Catherine will be interrogated with the addition of two new male characters. Like most tragic heroes, Eddie
Carbone possesses fatal flaws that are due to his uncontrollable emotions.
</p>

<p>Another means by which the playwright shows the protagonist experiencing hamartia is through his denial
and self-interest. Towards the end of Act 2, Eddie is directly confronted with reality as he “...can never have
her!”. The playwright aptly uses the accusatory tone of the exclamation to highlight “never”, emphasising that
Catherine is not within his reach. His response exhibits anger, as his “fists clench[ing]”, and denial because he
would never “...have such a thought?”. There is a moment of uncertainty through the questioning and the fact
that he is “shocked” hints that he is aware of this truth. Nonetheless anger, a negative by-product of Carbone’s
masculinity, subdues such feeling. Evidently, the concealing of emotions suggest an almost narcissistic
behaviour. Eddie’s denial is strengthened by his self-interest and obsession. Criticism of others is therefore an
intrinsic part of the protagonist which is put on display when he thinks his niece’s skirt “...[its] too short...”.
The following “ain’t it?” is a testament to his denunciation – a psychological justification for his action – once
more allowing audience members to glimpse inside the troubled, yet forthright, mind of Eddie Carbone. In
essence, Eddie Carbone’s self- interest and denial are his fatal flaw, however, in the end, also underpin
Alfieri’s statement and Eddie’s favourable qualities as he was for “himself purely” and allowed himself to be
“wholly known”. Most tragic heroes have an obsession (like power, domination, inflicting pain on others,
death...) that cloud their perception. In A View from the Bridge, the tragic hero’s self-interest, affection and
denial lead him to his demise.</p>
<p>Through Eddie’s respected stature in the community, the playwright effectively interweaves another key
aspect of Greek tragedies, while staying true to the modern significance of A View from the Bridge. The distinct
structure of the play aids in accompanying the gradual degradation of his respect. In Act 1, members of the
community admire and appreciate the risk that he accepts when taking in two “submarines”, therefore he has
“gotta lot of credit comin’ to [him].” The respect he gains from doing something illegal is of great importance
in his community. Nonetheless, Eddie’s quandary is that between culture (Italy) and the law (America).
Seemingly , Eddie knows the rules of the community. On the other hand, the protagonist’s need to be fully
American, and the current McCarthyism in the US during the writing of the play, ultimately lead him to
commit actions morally wrong in the community yet legally right. “Credit” is what Eddie wants, although it
becomes obvious that he cannot accept such recognition due to his fatal flaw; he becomes “troubled” when
Louis and Mike express their positive feelings towards Rodolpho. However, ultimately, Eddie gains the
respect of Rodolpho, as when he is asked to dance with Catherine, he responds “in deference to Eddie”. Due to
his affection to Catherine, her behaviour towards him and his ‘fatherly’ characteristics, he successfully
controls his niece’s view of the world, her respect. When told the story about Vinny Bolzano, Catherine
“...wont’ say a word to nobody...” This reflects and reassures Eddie’s previous statement that she should not
“trust nobody”, however should “believe [me]” him. The irony of these assertions that Miller creates through
direct language mirror the power Eddie has in the beginning of the play – he can manipulate views (to suit his
own) yet is still able to inject the trust of others into himself. This was most likely inspired by Miller’s own
experiences working in lower class areas of Brooklyn where the concept of community is integral, especially
within Italian American municipalities. Through this distinct social structure, the audience can assume, in
accompaniment with the constant foreshadowing, that conflict will break out in the second act, the
protagonist’s stature will be subverted, he will be faced with reality and, eventually, he will die. All these
aspects compliment the standard of a Greek tragedy and the course of downfall of a tragic hero.</p>
<p>
This raises another interesting point of the play through which the protagonist is able to thrive and present
himself as a “tragic hero” – community. A View From the Bridge is primarily told through the view of a family
in the neighbourhood of Red Hook, reflected by the set which is at a singular location (typical for Greek
tragedies). The restricted perspective metaphorically reflect Eddie’s clouded perception and naivety, enabling
the audience to see his traits viscerally and truthfully, although from behind the curtain of his hamartia.
Arthur Miller creates relief and a wider view of the situation through the character of Alfieri, who comments
on Eddie’s actions. Alfieri is a modern iteration of the traditional Greek chorus and accompanies the audience
throughout the play as an observant and wise spirit. He expresses what Eddie cannot say – the “...trouble that
would not go away.” – in pragmatic and insightful ways, attempting to also lead Eddie away from his
downfall. Nonetheless, he is aware that he is “powerless”. The title of the play, A View from the Bridge,
reinforces Alfieri’s position and creates contrast in connection with Eddie: “View” suggests the role that the
audience and Alfieri have in the play, while “Bridge” remarks relationships, setting (the Brooklyn Bridge) and
connections. Alfieri has a wider “view” of the area due to his experience in culture and the law and distance
to the conflict. In contrast, Beatrice comprehends the dispute slower as she has direct involvement and also
hesitates to admit Eddie’s misconduct due to his respected stature. The audience has a similar position to that
of Alfieri as they are able to see the happenings from multiple viewpoints. Arthur Miller exposes the life of a
family, giving members of the audience a glimpse into the privacy of households besides their own. In the
end, this private tragedy is determined on the streets of an interwoven community, highlighting another
integral part of Eddie Carbone being presented as a tragic hero – his downfall was indirectly his own fault
because he subverted culture and community trust. When Marco “pressed [the knife] home”, Eddie was still
holding onto it, symbolising the notion that he himself was responsible for his death. The consoling word
“home” connotes the freedom of the community because, in order to improve it, his death was required.
However, it also hints the liberty Eddie receives. His death shifted the anger and violence onto his cause of
death, Marco, leaving him with the role of a victim. In a way, this replenishes Eddie’s hubris as the
community can see justification in the protagonist’s need to betray culture and family. Through this, catharsis
is established and thus empathy as the punishment was not wholly deserved, like in many Greek tragedies.
Without community, the protagonist would not be able to exert his dominance (and through it his flaws), gain
self- knowledge and awareness and ultimately, not be presented as a ‘tragic hero’ due to the absence of a
suitable setting, other characters and a genuine community.</p>
<p>In the end however, the protagonist gains self-knowledge and completes his journey with a final solemn
discovery. Eddie is aware of his fate in his final line “My B.!”. “My” indicates possession and loss
concurrently and “B.!” expresses his final love for his true wife Beatrice. This last outcry of regret and
hopefulness once more establish Eddie as a tragic hero as, even though the event arouses solemn emotion, it
also offers catharsis and the audience is confronted with an understandable and genuine Eddie Carbone,
strengthening any sympathy the audience would have for the failed hero. Nonetheless, this “bloody”
happening is no surprise to viewers as it was foreshadowed in the beginning that the play would “...run its
bloody course.”. This</p>
<p>therefore brings the final essential aspect that makes A View From the Bridge a Greek tragedy and its
protagonist a tragic hero to the surface – the play was always about how Eddie Carbone would fall, rather
than the possibility of it. Yet, Eddie is not fully a tragic hero. Quintessentially, Miller challenges nobility and
wealth, characteristics that tragic heroes traditionally possess, in the protagonist of the play. Eddie Carbone is
a longshoreman of little material wealth, contrary to other tragic heroes. The playwright therefore brilliantly
establishes a connection between character and audience, complimenting Miller’s wish of writing a Greek
tragedy that appeals to the everyday man. Although wealth usually accentuates the downfall of the tragic hero
in Greek tragedies, the integral role that community plays in A View From the Bridge achieves a similar, if not
greater, sense of loss in Eddie Carbone’s demise. Arthur Miller deviates from traditional tragic heroes in
another way. Eddie allowed himself to be “wholly known”, a favourable quality. Alfieri hints however that
this was also “perversely pure” and that such exposure was “not purely good”. It can be said that, in a way,
the protagonist’s positive attributes reflect and influence his hamartia. This subversion is entirely different
from traditional tragic heroes who have explicitly separate and distinct qualities. Once more, Arthur Miller
exhibits human emotion and behaviour in a genuine and complex manner, paralleling a true and realistic
human being. In conclusion, A View From the Bridge attempts to highlight the importance of balancing
individual needs and desires with those of the wider community. Still implicitly, Arthur Miller, through the
presentation of Eddie Carbone, emphasises that downfall or degradation can happen to anyone, not
exclusively to the wealthy, and that one’s zealous attributes can contribute to their  demise.</p>


