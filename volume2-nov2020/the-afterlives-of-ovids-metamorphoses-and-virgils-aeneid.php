                    	<p>T.S. Eliot once said: “Dante and Shakespeare divide the world between them. There is no third.” Virgil and Ovid, however, divide the world of these two.</p>


<p>The Aeneid, Roman poet Virgil’s 19 BCE epic poem, narrates the hero Aeneas’s journey after the fall of Troy
and his suffering in order to found Rome. It was an ambitious work of social engineering, commissioned by
Emperor Augustus to assert his power after years of bloody civil war. After its publication, the Aeneid quickly
became the defining handbook of the Roman identity and remained the staple of the Latin classroom for
centuries to come. On the other hand, the Metamorphoses, Ovid’s culminating masterpiece, strung together
stories from the Roman mythological tradition into a single, genre-defying narrative. From Narcissus to
Achilles, these fifteen books of hexameter verse described and established the mythological heroes that we
know today. Both works, with their lucid prose and timeless imagination, inspired generations of artists and
writers. Beyond their vivid and dramatic scenes in paintings, however, the archetypes in the Aeneid and the
Metamorphoses were revived in later foundational works.</p>
<p>Throughout the Aeneid, Virgil explores the conflict between furor and pietas, passion and reason. Furor, from
which we derive the English word “fury,” can easily correspond to passion, uncontrolled emotion and
irrationality. Pietas is often translated as “dutifulness,” requiring the strict adherence to morality, stoicism in
the face of extremes, and resistance to give in to passion. These qualities align with the same principles of
acting upon reason. While Aeneas has traditionally been considered the epitome of pietas and of Roman stoic
ideals, a closer read suggests the duality of his character. When addressing his men, their ships and morale
battered by the sea, Aeneas “feigns hope in his face and stifles the pain deep in his heart” (Verg. 1.209). The
assurance of his men despite his own fears exemplifies Aeneas’s good Roman leadership and wavering, but
rational trust of the Fates. However, this is contrasted with his more flawed actions. In the ending scene of
the entire epic, Aeneas kills his enemy Turnus, who was already vanquished on the ground. He thus violated
his father’s warnings to “spare the conquered” (Verg. 6.853) in a burst of fury and desire to avenge Pallas,
whom Turnus had slain. This interplay between reason and passion formed the archetype of Aeneas’s
character.</p>
<p>In the first book of his seminal work, Divine Comedy, Italian poet Dante continued this archetype through the
two main characters, Dante and Virgil. The character Virgil, of course, was a homage to the revered poet
(while Dante named the protagonist after himself). Dante’s direct tribute to Virgil, nevertheless, went beyond
his name. For one, the character Dante’s storyline closely mirrored that of Aeneas’s in Book 6 of the Aeneid.
Both Dante and Aeneid journeyed to the Underworld with the guidance and protection of a more
experienced character (the shade Virgil in Inferno and the Sibyl in the Aeneid), crossing the river Acheron and
speaking with famed souls. Not only are their plots similar, Inferno probes the same contrast between passion
and reason through its characters. Dante’s character is driven by passion; he faints when frightened, weeps at
the shades, and sympathizes with sinners. When Dante pities the souls in the eighth circle of Hell, Virgil
admonishes him: “Art thou, too, of the other fools? / Here pity lives when it is wholly dead; Who is a greater
reprobate than he / Who feels compassion at the doom divine?” (Dante 132). Virgil, reminding Dante that
their sins are deserved, represents human reason. With his wisdom and composure, Virgil guides Dante
through Hell, instilling in him the rationality and morality needed to advance toward Heaven. With the
characters Dante and Virgil together, Dante continues the poet Virgil’s exploration of reason and passion
through the archetype of Aeneas.</p>
<p>The other half of Eliot’s world, Shakespeare, is constructed on the foundations of Ovid’s Metamorphoses. The
dynamic and relatable characters in Shakespeare’s plays are the afterlives of Ovid’s mythological heroes. Romeo
and Juliet, a cornerstone of English literature, was a transformation of Ovid’s Pyramus and Thisbe. In Ovid’s
tale, Pyramus and Thisbe are two forbidden lovers, divided by a wall and their parents’ rivalry. On the night
they plan to meet in secret, Thisbe see a lioness with a bloody mouth. She flees, leaving behind her veil, which
was torn apart by the lioness. When Pyramus arrived, he assumed that the lioness had killed Thisbe and
committed suicide out of grief. Upon her return, Thisbe stabbed herself, dying beside her lover. (Ov.
4.55-166). This narrative should be familiar to any reader, as Shakespeare closely followed the plotline in
Romeo and Juliet. Both works, recognizably, shared the archetype of “star-cross’d lovers” (Rom. prologue, 6).
Beyond simply a plotline, moreover, this archetype invokes the same psychological reaction in the reader in
both works. The reader feels anger at the parents for separating the lovers, pity for the doomed romance, and
perhaps most strongly, frustration at the easily-avoided misunderstanding that cost both characters their lives.
Ovid’s archetype of Pyramus and Thisbe is revived in Romeo and Juliet not only as names and the overall plot,
but with the same emotions and effects of the original work.</p>
<p>Another of Shakespeare’s fascinating pairs modeled after Ovid’s mythology is Helena and Demetrius. In
Book 1 of Metamorphoses, Ovid tells the story of Apollo and Daphne. Eros, the god of love, shot Apollo with
the gold arrow of passionate love and a nymph with the lead arrow of hatred. Apollo, overwhelmed with
desire, desperately chased the unwilling nymph Daphne. When Apollo finally caught up, Daphne prayed to
her father, the river god Peneus, and was transformed into a laurel tree. (Ov. 1.452-567). Shakespeare, in A
Midsummer Night’s Dream, narrates his derivative story of the pursuit of unrequited love. Helena chases
Demetrius, who scornfully warns her to “Tempt not too much the hatred of my spirit; / For I am sick when I
do look on thee” (MND 2.1.586-7). With the meddling of Puck, however, Demetrius is bewitched to return
Helena’s love. Although Shakespeare’s ending differs from that of Apollo and Daphne, he preserves the
theme of Ovid’s work. Metamorphoses, as the name suggests, is about transformation. Ovid opens his poem
with the lines: “My soul brings me to speak of forms changed into new / bodies” (Ov. 1.1-2). In the story of
Apollo and Daphne, Daphne was transformed into a laurel tree. In Shakespeare’s variant with Helena and
Demetrius, instead of a laurel tree, Demetrius undergoes a state of psychological transformation; namely, that
of love. Both of these transformations are achieved with the intervention of a non-human power (Peneus in
Ovid and Puck in Shakespeare). Through Helena and Demetrius, Shakespeare once again revives Ovid’s
archetype of unrequited love.</p>
<p>This recycling of previous archetypes does not dilute the importance of later works; rather, it contributes to a
rich literary tradition of allusions and connections. Each subsequent writer reads and builds upon the works
of predecessors with his or her own lens. Dante explored Aeneas’s contrast of reason and passion with an
undertone of sin and morality. Shakespeare incorporated the storyline and themes of Ovid’s mythology into
masterpieces of emotion and empathy. Dante’s Virgil is not Aeneas, and Shakespeare’s Helena is not Apollo,
but this renewal is what keeps these characters alive. Here lies the significance of Virgil and Ovid: they created
authentic, human characters to explore themes of human nature that we continue to grapple with in their
afterlives. As Ovid boldly declared in the ending of Metamorphoses: “Wherever Rome’s influence extends, I
shall be read by the mouths of the people, famous through all ages; if there is truth in the poet’s prophecies, I
shall live.”  (Ov. 15.876-9).</p>

