                    	<p>One topic that literary critics have long debated is whether or not the title is part of a work. Is the
title something separate, only useful for labeling and organization, or is the title crucial to the
interpretation of the work? Critic S.J. Wilsmore posits that a “work could be considered changed by
the title so that a different title means a different work” (Wilsmore 403). This suggests that the title
could make the novel more about a specific aspect of the novel, by drawing attention to that aspect.
F. Scott Fitzgerald’s <em>The Great Gatsby</em> has many aspects, but at its core tells the story of a complex
society, and a young man trying to get ahead. Fitzgerald struggled in choosing a name for his
multifaceted masterwork. After debating between many possible titles, and thereby possible
emphases, he eventually decided to put the focus on a character, rather than the system within which
that character operates. Calling the novel <em>The Great Gatsby</em> undermines a sweeping tale of American
wealth and society and turns it into a character sketch. Doing so also takes the focus away from
Fitzgerald’s vibrant descriptions of setting, and his brilliant use of color imagery. <em>Under the Red, White,
and Blue</em>, one of Fitzgerald’s alternate titles, w ould have made a better title for the novel, because it
emphasizes the novel’s focus on color and color imagery, and it points to the novel’s larger theme of
an unachievable American dream.</p>


<p>Just as Gatsby is a man rich with assets, <em>Gatsby</em> is a novel rich with color. From the “reflected gold”
(Fitzgerald 6) of windows to the “green light”(Fitzgerald 21) on the dock, Fitzgerald constantly uses
color to provide vivid descriptions of scenes and characters. The novel is a world of flashy, vibrant
parties, and flashy, overdone people. Colors give this world depth and realism, and the frequency of
them give it that over the top feel that was so peculiar to the roaring twenties. However, this device
does more than just provide atmosphere in the novel. Critic Robert Doak suggests that “color
imagery” can have an “effect... on structure, characterization, and theme.” (Doak 208) In <em>Gatsby</em> ,
Fitzgerald’s use of color points directly to the themes in the novel, especially to that of patriotism
and the American dream.</p>
<p>One of the central conflicts in the novel is the clash between “old money,” people who have grown
up rich , a nd “new money,” people who have made their own fortune. People’s possessions,
especially, houses are some of the things enlivened by vibrant color descriptions, and frequently
show their wealth and roots. The Buchanons’ house is “ a cheerful red and white Georgian Colonial
mansion overlooking the bay” (Fitzgerald 6). Red on its own may symbolize warmth or familiarity,
but as Doak explains, “the function of a single color image cannot be fully understood without
reference to all other color images in a work.” (Doak 208) The color red must be thought of as a
color surrounded by other colors. Together, these colors create a new theme. The bright brick
house, combined with the ocean and beach of the bay, comes together in a full image of American
red, white, and blue. This red familiarity, or antiquity, becomes something patriotic. Red makes old
money something that was established when the country was in its classic, “colonial” days. When
Jordan reminisces about her childhood in the south, remembering “red, white and blue banners in
front of all the houses” that waved in a “disapproving way” (Fitzgerald 74). Jordan grew up in
wealth, and remembers it fondly. Through memory (another type of antiquity) she paints old money
as something established and stable: mature like a “disapproving” parent would be. These examples
show how Fitzgerald’s use of color firmly links patriotism and the idea of old money.</p>
<p>When not describing the patriotic brick of old-money homes, the color red shows up almost rarely
through the novel. Twice it describes women’s hair, both times at parties, to add to the vibrant
splashes of color. Twice it refers to vaguely capitalist entities: once about “ocean-going ships,”
(Fitzgerald 68) maybe carrying wares to an overseas market, and once about gas station pumps. Both
of these things are related to business, one of the tenets of Gatsby’s relationship with the American
dream. Capitalist business is a crucial element of Americanism, and it is the avenue through which
Gatsby tries to achieve his dream. This red as business may show hope for him, but ultimately,
however, he fails. When Gatsby dies in passionate and reckless pursuit of wealth and success, his
dissipating blood traces a “a thin red circle” (Fitzgerald 162) in the pool water. When the
surrounding colors must be considered, à la Doak, the scene of Gatsby’s death becomes a tragic
American triptych, composed of blue water, a white mattress, and red blood. It is ironic that he
would die in such a patriotic scene, considering it was his chase after the distinctly American that
killed him. Just looking at the uses of red in the novel, the Under the Red of our alternate title
becomes something oppressive—old money will always be established and secure, forever above the
newcomer. The American system will never be conquered, and that “rags-to-riches story” which has
“become the central element in the American Dream” (Vanneman 266) is unachievable. However,
whether this stays true remains to be seen, as there are more colors to consider.</p>
<p>The second color in our alternate title, white is used significantly more than red— almost fifty times,
in fact. According to A.E. Elmore, it “makes more appearances in the novel than any other” color
(Elmore 428). Beyond describing paper, shirts, and dust, white is often employed as a descriptor of
characters’ skin or complexion, almost exclusively of the established wealthy, to represent purity.
Daisy is pale white, and Jordan is “powdered white over [her] tan” (Fitzgerald 116). Jordan refers to
a more innocent time as her “white girlhood” (Fitzgerald 19), and Daisy and Jordan dress almost
exclusively in white, gauzy dresses, practically oozing innocence as they recline in chosen ignorance.
More than a pureness of womanhood, white also brings to mind the glory of God, or of heaven.
Elmore posits that East Egg is “the local heaven.” (Elmore 430) In this way, old money is equated
with God and goodness. The upper class is higher in more ways than one— they reach for heaven.
In addition, this pure whiteness has another, somewhat more sour connotation. Another facet of
whiteness is the so-called purity of race, which Tom is obsessed with. Tom fears that the “white
race” will be “submerged” through interracial marriage. (Fitzgerald 13) In Gatsby, the color white
means purity and cleanliness, and also points to the wealth of the upper class. There are no gaudy
colors here. Furthermore, for better or for worse, the United States was built on a foundation of
deeply religious, and sometimes deeply racist, ideals. So the use of the color white also points toward
that theme of deep Americanism. <em>Under the Red and White</em>, then, would make this novel into a story
of those living beneath the established American wealthy— those whose purity is unmatched and
who exclusively are able to reach heaven. With these two colors only, our tragic hero Gatsby faces a
massive, unbeatable system where the old rich will always be better and higher than everyone else.
However, there’s one more color left.</p>

<p>Finally, blue. It’s in the ever-present sky, the water of the bay, and Tom’s car. it’s a classic color,
something taken for granted in the beachside landscape. Something dull enough, and yet dignified
enough, to carry Tom on his various errands. Blue often fades into the background of the novel—
Tom’s car is described as “easygoing,” (Fitzgerald 125), and there’s vague “blue smoke” in the air
after Gatsby’s death. (Fitzgerald 176) Even the massive eyes of Dr. Ecklenburg are “dimmed” and
faded. The blue in Gatsby is understated but always there, and the inclusion of it in a consideration of
color brings the reader to a patriotic conclusion. <em>Under the Red, White, and Blue</em> : The addition of blue
is where Gatsby gets his chance. With blue, the power of the high class is faded, dimmed, and the
trio of American colors is finally complete and points toward that elusive <em>American dream</em> which is so
important in the novel . The established is fading— that classic “rags-to-riches” (Cannon 266) story
is possible after all. It is possible, but can it be achieved?</p>
<p>According to Roger L. Pearson, the American dream “is the belief that every man, whatever his
origins, may pursue and attain his chosen goals,” especially “political, monetary, or social.” (Pearson
638) American society has always been one with built-in class mobility, and during the economic
boom of the 1920s, the opportunity to improve one’s social standing seemed especially possible. An
optimist looking at the capitalist economic system would say that anyone could attain their goals,
make their fortune and achieve high class standing. However, <em>Gatsby</em> , with its tragic fall of a
high-achieving dreamer who tried but could not find success, presents a more cynical look at the
American dream. Professors Cannon and Vanneman, in their book The <em>American Perceptions of Class</em> ,
agree with this point, arguing that the classic American “mobility promotes dissension rather than
stability.” They go on to say that those in a higher class raise “expectations,” or customs, that the
dreamer will never be able to grasp. (Cannon 259) These established, pure roots leave Gatsby always
just one step behind. Though the power of the upper class may be dimming, it has not yet dimmed
enough for the dream to be achievable. The novel paints the American dream as just a dream, with
its chasers doomed to always remain <em>under</em>. Success is still highly unlikely, and gloriously failed, with
Gatsby’s violent death a “haunted” thing that sours the idea of the American dream forever
(Fitzgerald 176).</p>

<p>Though the character of Gatsby may be a stellar example of a failed American dreamer, he is not the
most important part of <em>The Great Gatsby</em>. Much more important is the system in which Gatsby failed.
Since “the sweep of the story...covers much of what was modern America at the time” (Nagel 123),
the novel should not be so narrowly defined as <em>The Great Gatsby</em>, a novel about a man. It is not just
about the tragic death of man, but instead is a cynical picture of the entire system, and how the
American dream can be reached for, but not grasped. Fitzgerald’s use of color points to this theme
all throughout the novel. <em>Under the Red, White, and Blue</em> is therefore a much better title, as it both
points the reader to the importance of color, and emphasizes the distinctly American system that the
novel critiques. Finally, that preposition <em>under</em> points to the novel’s climactic tragedy, and its
largest—those working within the American system will always be below, beneath, and never truly
achieve the lofty heights of their goals. The American dream is just that: a dream.</p>

