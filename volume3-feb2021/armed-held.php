                <p><em>“Unless carefree, mother-love was a killer.”</em></p>
                <p><strong>Toni Morrison, Beloved</strong></p>
                    	<p>The separation of Black mothers from their children in the United States began with slavery and
continues in its wake; the traumatic financial, emotional, and ancestral impacts of slavery all
contribute to the adversity Black motherhood is subjected to. Heightening this hardship is the
objectification and commodification of Black bodies, particularly Black women’s bodies, which
began with slave labor and persists in racial violence and violence against women; in Jazz , this
violence, and the resistance to it, appears in themes of hunting and “armed women,” respectively.
Jazz explores the origins, lives, and legacies of Black women through the theme of motherhood and
family; a history of slavery and violence haunts its characters, who must become “armed women” in
order to survive.</p>
<p>The Black women of Jazz are fragmented and unsound; transgenerational cycles of violence and
death have left them pained and searching for solace, an instability both caused and compounded by
the effects of racism. The narrator describes Violet’s volatility through her “private cracks” (p. 22),
moments of splintering in otherwise quotidian settings, during which she loses her grasp on identity
and place: “there is no foundation at all, but....ill-glued cracks and weak places beyond which is
anything. Anything at all. Sometimes when Violet isn’t paying attention she stumbles onto these
cracks...” (p. 23). Violet behaves erratically and unpredictably in a “crack;” she sits down in the street
unable to walk; she steals a baby from its stroller. Violet is child-less, a choice she is secure with until
she reaches the age at which she can no longer bear children. At this point, “Violet started sleeping
with a doll in her arms” (p. 129). Violet begins to think of her “cracked” self as a separate entity--
“ That Violet” (p. 90). This radical separation of mind from action displays dissonance for Violet
between what she has and what she desires: when she steals the baby, she desires a child; when she
attacks Dorcas’ corpse, she desires revenge. Mirroring this separation is much of Violet’s
relationship to Joe Trace: “Who was he thinking of when he ran in the dark to meet me in the cane
field? Somebody golden.... from the very beginning I was a substitute and so was he” (p. 97).
Morrison brings into this conversation of divides and desires the notion of whiteness; Violet
attributes her own instability, the way she “messed up [her] life” (p. 208), to her longing to be
“somebody else....White. Light” (p. 208), later tying this description to Golden Gray, the
white-passing son of Vera Louise and Henry LeStory who was cared for by Violet’s grandmother.
Stability, then, is whiteness (or, to some extent, the appearance of whiteness) and maleness: to be a
white man in America is to have roots, a solid foundation. Joe and Violet met beneath a tree: “the
one whose roots grew up its trunk” (p. 180). The tree is not firmly grounded, relying only upon itself
to stay upright: “[its] roots grew backward as though, having gone obediently to the ground and
found it barren, retreating to the trunk[....]Toward [...] light” (p. 182). It is emblematic of a Black
American struggle with identity and self as well as subsistence, as Black characters in Jazz reckon
with the loss of their history-- a loss deeply tied to the separation of families, through chattel slavery
and its aftereffects-- and cope with the pain of American Blackness: the disproportionately unstable
livelihoods produced by systemic racism. The narrator states: “the past was an abused record with
no choice but to repeat itself at the crack” (p. 220), tying all this brokenness and rootlessness to a
history of discrimination and brutality.
</p>

<p>Morrison chooses to hone in on the pain of Black mothers, in particular. Far from these safer
identities of whiteness and maleness, Black women in Jazz are wandering and wounded.
Motherhood as a Black woman is distinctly challenging, as evidenced by so many Jazz characters’
orphanhoods-- Rose Dear, for example, is driven to suicide by the hardships of motherhood,
exacerbated by the effects of racism, as speculated by Violet:</p>
<p>“What was the last thing...she had not been able to endure or repeat? Had the last washing split the
shirtwaist so bad it could not take another mend....Perhaps the word had reached her about the
four-day hangings in Rocky Mount: the men on Tuesday, the women two days later.” (p. 101).</p>
<p>
After this consideration of her mother’s suicide, Violet resolves to “never never have children” (p.
103), recognizing the pain and peril of Black motherhood. Similarly, after giving birth to Joe, Wild
refuses to hold or feed him and is regarded as a failure and a nuisance to the townspeople: she was
“too brain-blasted to do what the meanest sow managed: nurse what she birthed.” (p. 179). How,
though, is one expected to raise children that could one day be taken from them, in a manner
reflective of chattel slavery? Those children orphaned and wounded, “the children who made a
mistake in the parents they chose” (p. 35), the mistake of being Black in America.</p>
<p>Black mothers in Jazz must fend for themselves, then; already fractured by their world, they are met
with the immense challenges of motherhood under oppression. Morrison returns throughout the
novel to the notion of “armed” Black women; “violent,” these women look out for themselves and
their own survival. Black women in Jazz are under threat-- their armed-ness is an attempt at
self-preservation: “‘I don’t understand women like you,” said Alice Manfred to Violet, “women with
knives.’ ‘I wasn’t born with a knife’ [said Violet]. ‘No, but you picked one up’” (p. 85). Conversely,
un -armed women are just as broken; Neola Miller, the “one-armed” (p. 61), devoutly religious friend
of Alice Manfred, “held the broken pieces of her heart together in the crook of a frozen arm” (p.
62). Beyond their armedness, Black women’s bodies are described as inherently violent. To Golden
Gray, it appears that “Everything about [Wild] is violent, or seems so, but that is because she is
exposed under that long coat” (p. 153); the history of objectification and violence against Black
women in American history pervades their physical appearance to others, impacting every aspect of
their being-- even their names, as Violet comes to be called “Violent” (p. 79). Golden Gray
attributes a Black woman’s violence to her nakedness; to him, Wild’s exposed body is inherently
brutal, threatening-- savage. To Joe, her body is something to be hunted for sport: “Joe had been
joking, speculating on what it would take to kill Wild” (p. 175), akin to the commodification of Black
women’s bodies in slavery. However, Joe’s speculations come to an abrupt end with Henry LeStory’s
allusive response: “‘You know, that woman is somebody’s mother and somebody ought to take care’” (p.
175). To Joe, the value of Wild’s body lies solely in her relationship to him-- he only views her as
anything more than amusing prey when confronted with the possibility of being her son. The
suggestion of this relationship is all that protects Wild from, hypothetically, being hunted-- her body
is a commodity and an object to society, just as Black women’s bodies were under slavery, and as
they continue to be in these rigid confines of systemic inequality.</p>
<p>Entwined with the theme of constricted Black womanhood is the motif of dark and narrow spaces;
these are deep, contained, isolated, other-- they are the places armed, fractured women go. Whether
they make a home of these places, as Wild in her cave, or stumble into them by accident, as Violet
and her cracks, or, like Rose Dear, seek reprieve at the bottom of a well, the theme persists: they are
a space for wounded Black women to be held. While holding the baby she took from its stroller,
Violet is described as feeling a "brightness that could be carried in her arms. Distributed, if need be,
into places dark as the bottom of a well" (22), emphasizing the contrast between white, light stability
and the darkness and volatility of Black womanhood. Morrison’s mention of a metaphorical well is
deliberate, and key in understanding the trouble with Black motherhood in Jazz ; like her mother,
Violet feels the pull of the well that “sucked her sleep” (p. 102), leaving her restless. She describes
the pain and darkness of Black womanhood, calling out: “Mama? Mama? Is this where you got to
and couldn’t do it no more? The place of shade without trees...” (p. 110). Time and time again, Jazz ’s
Black families are shattered, their mothers unable to “do it;” to bear the burden of mothering
another generation of “hungry [mouths saying] Mama?” (p. 102). While Violet’s dark spaces are
threatening cracks, Wild and Rose Dear seek shelter in these spaces, the distinction between these
women being that Violet never becomes a mother. Therein lies the difference between Black women
and Black mothers; while Black women are subject to the same darkness and fragmentation of the
self caused by transgenerational cycles of violence and pain, Black mothers have no choice but to
perpetuate this cycle, and must find a means of coping with this weight. Rose Dear, driven to suicide
by Black womanhood and motherhood, chooses the well as her escape; in this picture of Rose Dear
lying dead and cramped at the well’s base, “it was pure, breathing relief to see her stretched [in her
casket]” (p. 101), illustrating the extent of her constraint, with freedom found only in death. Wild,
too, chooses to make a home out of a dark, remote “burrow” (p. 183). While the Black mothers of
Jazz desire the stability of this “light” white maleness, they find real security in seclusion and
darkness.</p>

<p>The peacefulness of Wild’s burrow-home not only contradicts her foreboding and savage name, but
also the white notions of acceptability and security presented throughout the novel. Morrison
depicts true comfort in these spaces for Black mothers; though “wild” and even repugnant to
spectators, they bring a feeling of safety to their inhabitants. Morrison leaves Jazz on a more loving
note; one of the parting images of Joe and Violet is sweet and homey, depicted sleeping side by side
in their home. In this moment of peace, “Violet rests her hand on her chest as though it were the
sunlit rim of a well” (p. 225); with her arm in the gesture attributed to Neola Miller’s shattered heart
and a connection to her mother’s suicide, Violet feels tranquil and loved-- even safe. Wild’s
burrow-room is described affectionately by the narrator as: “That home in the rock; that place
sunlight got into most of the day....a place already made for me, both snug and wide open....Unseen
because she knows better than to be seen....would see her, a playful woman who lived in a rock?
Who could, without fright?” (p. 221). This home, “both snug and wide open,” is as close to freedom
from oppression as Wild may ever get. It is a “home in the rock;” a small piece of shelter in a rigid
and unforgiving world. Here, unseen by judging, hunting America, a Black mother finds peace.</p>

<p>Black motherhood in Jazz is suffocating, painful, and cyclic; these mothers seek to escape the
transgenerational cycles of abuse and exploitation tied to America’s history of slavery. The few
moments and places of security they do find are unconventional, to say the least, but provide what
comfort can be found for Jazz ’ s disenfranchised, victimized Black mothers in a country built on the
backs of their enslaved ancestors. Yet Morrison manages to end the novel on a positive note: despite
all the pain that accompanies it, and despite how unbearable its burden, mother-love is still there-- a
beacon of sunlight on the rim of a well-- hoping for this, its new life, to be better than the last.</p>


