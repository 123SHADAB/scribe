                    	<p>Food transcends physical sustenance. It feeds a social construct that places men above women,
white above Black, and rich above poor. Ultimately, it announces social status and wealth. Does a
plate hold rainbows of veggies alongside clean-cut lamb? Or bits of days old rice? Just as how a few
indulge while the rest starve, the elite splurge on prosperity while the lowly sort through their scraps.
This social hierarchy appears in Charlotte Brontë’s Victorian novel <em>Jane Eyre</em> , which starves Jane of
her independence through malnutrition . In contrast, Jean Rhys’ postcolonial perspective in <em>Wide
Sargasso Sea</em> rewards Antoinette’s submission with temporary abundance. Though Brontë’s and
Rhys’s heroines dine in different manners, they both ultimately ingest the same truth: food unveils
social status.</p>


<p>Both authors use a shift in class to crush Jane’s and Antoinette’s innocence and expose them to
social structure. Young Jane “devour[s]” her first meal at Lowood “without thinking of its taste”
until she suddenly registers a “nauseous mess of burnt porridge.” (Brontë 55). At Gateshead, she
only ever knew wealth, private cooks, and quality meals. She may have lacked a family connection,
but her experience at the dinner table had only ever been privileged. She carries this upper-class
mindset to Lowood where she “devours” the first meal without questioning its palpability. A rich
girl turned poor, Jane finally learns that quality food is a perquisite reserved to the rich, which she no
longer enjoys. Inversely, Antoinette encounters a sudden gain in wealth that exposes this same truth.
Her mother’s recent marriage forces her to adopt an English diet of “beef and mutton, pies, and
puddings.” (Rhys, 32). Annette’s former maidenhood categorized her family as low-class because
they lacked a financial male caretaker. Just as how an upper-class childhood develops Jane’s
privilege, Antoinette’s lower-class background guarantees a similar innocence. Her younger self
enjoyed “Christopine’s cooking” because that was the only cuisine she had ever known. However,
Mr. Mason barrels into her life as a landowner and member of the gentry ready to cleanse her West
Indian experience with “superior” British class. Though her meals are lavish and social status
elevated, Antoinette’s newfound luxury comes at a cost. She will never taste “Christopine’s cooking”
again because her new social class binds her to new customs. Whether rotten or lavish, food ruins
the taste of Jane’s and Antoinette’s innocence, fastening each heroine to her perspective rung of the
social ladder.</p>
<p>As both Brontë and Rhys withhold luxury from the lower classes, the lavish meals reveal that
classism perpetuates itself by manipulating the poor. When Miss Temple treats her pupils to “bread
and cheese instead of burnt porridge,” Mr. Brocklehurst insists that such comfort “starve[s] [the
girls’] immortal souls!” (Brontë 75). The Victorian elite enjoyed luxuries like cheese, and the
middle-class ate bread. According to Mr. Brocklehurst, both these meals will not suit the girls. If
they cannot eat luxury food nor that of the middle-class, what can they eat? Burnt porridge emerges
as the final option. These impressionable young girls, like other vulnerable members of their class,
grow up viewing lowliness as their sole way of survival. They never question this system out of fear
that rebellion may incite harm, showing that classism survives by preying upon the human instinct
for survival. Likewise, impoverished communities in the West Indies face this same predicament.
Servant girl Amélie spoon-feeds Mr. Rochester “cold chicken, bread, fruit, and a bottle of wine”
(Rhys 126). This meal is huge and already prepared. Hard at work and probably hungry, Amélie
dares not ask for a bite. Such a question would violate social customs as Rochester is a rich white
man and she a poor servant girl. This one-sided opulence reflects the hopeless situation of the lower
classes. Many poor people perform difficult sometimes back-breaking labor but only receive
minimum wage. Despite her hardwork, Amélie only prepares the opulence for others and never
herself.</p>
<p>Whereas poverty breaks down Jane’s body, Antoinette’s mind pays the price of her wealth, proving
that each class endures distinctive suffering. Jane hits rock bottom after she begs a little girl for a
“mess of cold porridge [about to be thrown] into a pig trough” (Brontë 379). Leaving Rochester
stripped Jane of her job and potential suitor. Her humiliating, pitiful request reveals the extent of her
desperation as she begs for animal slop. Victorian class structure once again forces the poor to fend
for themselves in whatever manner they must. This toxic system also harms wealthy Antoinette.
When Rochester locks her in Thornfield like an animal, she dulls her pain by downing “the drink
without color” (Rhys 161). This alcohol affects her within a few sips, which reveals her access to
high-quality alcohol and other luxury goods. However, she also harbors a certain pain to pay for
such access. Eager suitors are a common perquisite of wealthy women, and Antoinette’s wealth
attracted Rochester in the first place. Though protruding bones may not announce it, Antoinette
suffers alongside Jane due to her class.</p>
<p>In their novels, Brontë and Rhys highlight a stark reality. Just as how everyone depends on food for
survival, all classes suffer at the hands of social structure. Wealth sets Antoinette on a path towards
destruction. Poverty thwarts any chance of Jane’s for financial independence. Whether it propels us
forward or holds us back, social status exerts a dangerous amount of influence over our fate. It
decides our place in society, robbing us of the chance to create who we are and who we hope to
become.</p>

