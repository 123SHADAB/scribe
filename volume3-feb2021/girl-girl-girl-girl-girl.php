                    	<p>In 1972, when Toni Morrison wrote <em>Sula</em> , the civil rights movement was still a recent memory. One
of the most powerful images from that time – African Americans marching with locked arms against
the oppression that sought to divide and destroy them – does not directly appear in Sula , but
Morrison still echoes its sentiment in her portrayal of the unique friendship between Sula Peace and
Nel Wright. Just as the civil rights marchers rebelled against the competitive white society by
expressing their unity, Nel and Sula also resist the demeaning strictures of the Bottom by creating a
friendship built on the harmony between their two minds. Morrison presents these two girls’
relationship as a private oasis amongst the rivalries of the outside world, a refuge in which they have
room for meaningful self-growth in the midst of a society that seeks to dull down their individuality.</p>


<p>Morrison marks Nel’s introduction in the novel by giving her a sense of self-discovery that opposes
her mother’s weaknesses in the face of white racism. On the train to New Orleans, the white
conductor berates Nel’s mother, who takes his cruelty without resistance. This episode causes Nel to
wonder “if [her mother] were really custard, then there was a chance that Nel was too”(Morrison
22). Nel, therefore, begins to see herself in terms opposed to her mother. In an effort to separate
herself from the passive woman who wilts before the white conductor on the train, Nel views her
mother’s “custard” skin as a symbol of weakness. From this point onwards, Nel is determined to
embrace her own strength and individuality; in other words, to define herself as someone apart from
her mother.</p>
<p>Nel’s newfound confidence and rebellious nature leads her to engage in a somewhat forbidden
friendship with Sula. For years, Nel had seen Sula but had never interacted with her because her
mother claimed that Sula’s mother was “sooty.” This designation carries implications of dirtiness
associated with Sula’s family, framing them as outcasts to society who should be considered lesser
than the Wrights. Nel’s mother here is acting in accordance with the class system in society, and
seeks to mark certain others as “lesser” than herself. The white conductor demonstrated the same
nature when he verbally abused Helene on the train. However, by disregarding Sula’s social status
and befriending her, Nel uses the strength of her individuality to rebel against not only her mother,
but also the larger competitive nature in society: “her new found me-ness, gave her the strength to
cultivate a friend in spite of her mother” (Morrison 29). Nel forming her friendship with Sula “in
spite of her mother” further allows her to develop a rebellious sense of self. By incorporating
incidents featuring one party putting down another, Morrison highlights the general nature of this
society as everyone fights to be at the top. Even so, an alternative lifestyle is introduced, one that
holds the promise of unity: Nel and Sula’s friendship.</p>
<p>Morrison goes on to emphasize the constructive and creative nature of Sula and Nel’s friendship,
qualities that are contrasted with the destructive nature of conflict present in the Bottom. She writes
that “because...all freedom and triumph was forbidden to them, they had set about creating
something else to be... [they used] each other to grow on” (Morrison 52). The development of the
friendship is described in active terms as they themselves are “creating” it, starting something new
that goes against the norms of the rest of society. This “creation” is depicted as the result of other
forms of happiness – such as “freedom and triumph” – being “forbidden to them.” Their
constructive act of self-hood is therefore a novel and subversive relationship in the midst of society.
As shown by the behavior of the conductor and Nel’s mother, competition appears not only
through a pressure on class structure but also in other events unfolding in the background. Shadrack
is devastated by the competition of war; various Black men have trouble finding work due to
competition amongst themselves and white workers; and girls constantly compete for the attention
of men.</p>
<p>At the height of their friendship, there is no such competition between Nel and Sula, and this
absence of tension leads to a rare unity between their two minds and positive self development.
During a time when most girls compete for a man’s love, Morrison describes Nel and Sula as they
“never competed against each other for them” (Morrison 84). She uses their lack of opposition to
imply a strong bond, such that their consciousnesses meld together. They have trouble
“distinguishing one’s thoughts from the other’s,” as if their minds have become one larger mind
(Morrison 83). In order to truly define themselves apart from the way that the racist society seeks to
define them, Nel and Sula must come together privately in their minds. Morrison reinforces this link
only ten pages later, when, while describing Nel’s reflections about her friend, she mentions that
“Sula never competed; she simply helped others define themselves” (Morrison 95). The association
between the ideas of non-competition and self definition is emphasized by the semi-colon, which
conveys a connection rather than the division that a period might represent. It is difficult to
determine where Nel’s self ends and where Sula’s begins: “Talking to Sula had always been a
conversation with herself” (Morrison 95). It is clear how absenting oneself from the destructive
pressures of competitive society allows one to engage in a growing relationship.</p>
<p>However, this unity is only temporary as the dark threat of competition – in the form of Nel’s
husband Jude – inserts itself between Nel and Sula and drives them apart, devastating Nel’s sense of
self in the process. Soon after Nel and Jude’s wedding, Sula goes to college. Upon her return, she
has a different outlook on life and feels far above the rest of the Bottom. Sula’s attitude is one of
utmost freedom, as if she has the right to do anything, and it leads her to cheat on Nel with Jude.
Sula and Nel now compete for attention from the same man, an idea that was once absent in their
friendship. Nel is heartbroken in more ways than one. What is most troubling is that she is forced
“to lose Jude and not have Sula to talk to about it because it was Sula that he had left her for”
(Morrison 110). This reference to Nel’s inability to “talk to” Sula about her devastation contrasts
with the earlier description that “talking to Sula had always been a conversation with herself.” When
considering these two quotations together, we realize that in destroying the friendship, competition
has also ruined Nel as an individual, depriving her of the “self” she could formerly talk to. Morrison
incorporates competition as a force that divides white from Black and friend from friend. She seems
to imply that there is no place for rivalry in a true friendship that Nel and Sula once had. This
profound sincerity that had once built their relationship to the point that Nel and Sula were almost
becoming one person, but tragically, competition eventually breaks this relationship apart.</p>

<p>Upon Sula’s death at the end of the novel, she no longer poses a threat to Nel. The sense of
competitiveness between them fades away, allowing Nel to finally remember the former sense of
strength between them. On the novel’s final page, soon after Sula’s funeral, Nel can’t help but
tearfully acknowledge the bond she once held with her friend, crying out “We was girls together...
girl, girl, girlgirlgirl” (Morrison, 174). The lack of spaces between the repeated instances of the word
“girl” demonstrate not only Nel’s passion in this moment, but also highlight the way in which the
shared girlhood between two friends was powerful enough to overcome traditional divisions in
society. The real tragedy of this novel, however, is that once Nel finally understands how much she
misses Sula, it is too late.</p>

<p>Throughout her novel, Morrison presents an ideal harmony between two best friends, in which
there exists no competitive nature, otherwise present in the rest of society and more largely, the
human race. Yet Morrison also indicates in her last few pages that this ideal “perfect” relationship is
not permanent. Through this implication, she demonstrates the enduring power of a competitive,
racist, and hierarchical society to undermine the shared private rebellion of two girls who wished to
be their own individuals. Even if Nel and Sula attempted to rebel against these traditions by forming
a candid friendship, they did not ultimately prevail. For a while, however, they were able to find the
promise of a life that was not defined by a society so hostile to the idea of two Black girls
flourishing.</p>

